              <p>Joint mobilization is a form of manual therapy used by physiotherapists to improve mobility, increase range of motion and decrease overall pain. Following injury and immobilization, soft tissues may become shortened and limit overall movement and restrict joint functionality. By performing joint mobilization, which is a low-grade, passive movement of a skeletal joint, it helps restore and improve functionality in that joint.</p>
              <p>Joint mobilization is not a painful technique, it is a gentle, small movement within the joint capsule. By performing these passive movements within the joint, this produces more synovial fluid (fluid within the joint) which helps lubricate and bring in nutrients in the area to enable faster healing.</p>
